// Задание 7:
// Бизнес-ситуация: Компания хочет заставить менеджеров качественнее заполнять карточки Клиентов (Customer). Если менеджер создает нового клиента или редактирует существующего, система в момент нажатия кнопки «Сохранить» должна проверить: если у клиента заполнен ИНН/Налоговый номер (vatregnumber), то поле «Комментарий к аккаунту» (comments) тоже обязательно должно быть заполнено (там менеджер должен указать налоговые особенности).
// Если ИНН есть, а комментария нет — скрипт должен заблокировать сохранение карточки и выбросить на экран понятную ошибку.
// Условия:
// - Тип скрипта: User Event Script.
// - Режим работы: Standard Mode (так как мы работаем со свойствами текущего контекста).
// - Точка входа: beforeSubmit — это системный триггер, который срабатывает на сервере после того, как пользователь нажал кнопку «Сохранить», но до того, как данные физически записались в таблицы СУБД. Идеальное место для финальных проверок.

/**
 * @NApiVersion 2.1
 * @NModuleScope SameAccount
 * @NScriptType UserEventScript
 */
// запрос модуля 'N/record' (для работы с записями) и модуля 'N/error' (для создания и генерации ошибок)
define(['N/error'], (error) => {
  /**
   * Функция, выполняемая перед записью данных в БД.
   * @param {Object} context
   * @param {Record} context.newRecord - Новая запись
   * @param {Record} context.oldRecord - Старая запись (только при EDIT, DELETE и др.)
   * @param {string} context.type - Тип триггера (CREATE, EDIT, COPY и др.)
   */
  // обязательная функция/логика, которую User Event Script будет отрабатывать.. т.е. такая, точка входа (Entry Point)
  const beforeSubmit = (context) => {
    // ?? проверка типа события.. выполнение валидации только при "создании", "редактировании" или "копировании" записи
    if (
      context.type === context.UserEventType.CREATE ||
      context.type === context.UserEventType.EDIT ||
      context.type === context.UserEventType.COPY
    ) {
      // получение записи клиента (с которой идёт взаимодействие) из контекста..
      const customerRecord = context.newRecord;
      // получение значения поля ИНН (vatregnumber)
      const vatNumber = customerRecord.getValue({
        fieldId: 'vatregnumber', // ID поля ИНН/Налогового номера
      });
      // получение значения поля "Комментарий" (comments)
      const comments = customerRecord.getValue({
        fieldId: 'comments', // ID поля комментария
      });

      // !! организация проверки.. если ИНН заполнен (НЕ пустой), а вот комментарий "наоборот".. не заполнен (ПУСТОЙ)
      if (vatNumber && !comments) {
        // выброс системной ошибки.. согласно возможностей модуля 'N/error' (это "выброс" будет виден пользователю)
        throw error.create({
          name: 'MISSING_COMMENTS', // уникальное имя (код) ошибки
          message: 'Заполнение поля Комментарий обязательно при указанном ИНН!', // сообщение для пользователя
        });
        // ?? Инструкция throw error.create генерирует жесткое системное исключение. Движок NetSuite видит, что скрипт упал, немедленно выполняет команду ROLLBACK в СУБД (стирает временные данные) и выводит текст из параметра message на экран пользователю в красивом красном блоке. Запись в базе остается нетронутой.
      }
    }
  };

  // обязательный возврат функции/метода.. beforeSubmit, т.е. перед сохранением записи (перед "Save" в UI)
  return { beforeSubmit };
});
